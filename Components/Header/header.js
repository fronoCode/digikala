class Header extends HTMLElement {
    constructor () {
        super()
        
        this.attachShadow({mode : 'open'})

        this.shadowRoot.append(template.content.cloneNode(true))
    }
    async connectedCallback () {
        const loginBtn = this.shadowRoot.querySelector('.login-btn')
        const a = this.shadowRoot.getElementById('login-page')

        if (document.cookie.includes('userID')) {
            let findIndexCookie = document.cookie.indexOf('=')

            let userID =  document.cookie.slice(findIndexCookie + 1,)

            a.href = 'profile.html'

            // ______________________________________________________________

            let res = await fetch('https://itsrealdatabase-default-rtdb.firebaseio.com/phone.json')
            let jsonUser = await res.json()

            let arr = Object.entries(jsonUser)

            let findID = arr.find(i => +userID === i[1].id)

            

            // ______________________________________________________________

            loginBtn.innerHTML = findID[1].userName


            loginBtn.classList.add('active')
            
        } else {
            loginBtn.innerHTML = `<svg xmlns="http://www.w3.org/2000/svg" width="16" height="16" fill="currentColor" class="bi bi-box-arrow-in-left" viewBox="0 0 16 16">
            <path fill-rule="evenodd" d="M10 3.5a.5.5 0 0 0-.5-.5h-8a.5.5 0 0 0-.5.5v9a.5.5 0 0 0 .5.5h8a.5.5 0 0 0 .5-.5v-2a.5.5 0 0 1 1 0v2A1.5 1.5 0 0 1 9.5 14h-8A1.5 1.5 0 0 1 0 12.5v-9A1.5 1.5 0 0 1 1.5 2h8A1.5 1.5 0 0 1 11 3.5v2a.5.5 0 0 1-1 0z"/>
            <path fill-rule="evenodd" d="M4.146 8.354a.5.5 0 0 1 0-.708l3-3a.5.5 0 1 1 .708.708L5.707 7.5H14.5a.5.5 0 0 1 0 1H5.707l2.147 2.146a.5.5 0 0 1-.708.708z"/>
            </svg>
            ورود | ثبت نام`

            a.href = 'login.html'
            loginBtn.classList.remove('active')
        }
        this.hiddenNavBar()
    }
    hiddenNavBar () {
        const nav = this.shadowRoot.querySelector('nav')
        const container = this.shadowRoot.querySelector('.container')

        window.addEventListener('scroll',e => {

            if (document.documentElement.scrollTop > 1) {
                nav.style.opacity = 0
                container.style.height = '70px'
            } else {
                nav.style.opacity = 1
                container.style.height = 'auto'
            }
        })
    }
}

